#include "ft_printf.h"

int	handle_str(char *s)
{
	int	bytes;

	bytes = 0;
	if (!s)
		return (bytes);
	while (*s)
	{
		bytes += handle_char(*s);
		s++;
	}
	return (bytes);
}
